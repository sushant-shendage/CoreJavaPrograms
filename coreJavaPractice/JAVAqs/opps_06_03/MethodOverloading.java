public class MethodOverloading {

    void method1(int a) {
        System.out.println(a + " is an integer value");
    }

    void method1(float a) {
        System.out.println(a + " is an float value");
    }

    void method1(double a) {
        System.out.println(a + " is an double value");
    }
    void method1(char a) {
        System.out.println(a + " is an char value");
    }
    void method1(String a) {
        System.out.println(a + " is an String value");
    }

}
