public class Encaps2 {
    private int a, b;

     

    Encaps2(int a, int b) {
        this.a = a;
        this.b = b;
    }

    int getA() {
        return this.a;
    }

    int getB() {
        return this.b;
    }

    void setA(int a) {
        this.a = a;
    }

    void setB(int b) {
        this.b = b;
    }
}
